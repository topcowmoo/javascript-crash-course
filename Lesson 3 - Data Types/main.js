// Data Types

// Primitive types:

// String type 
// Number type 
// Boolean type 
// Undefined type
// Null type
// BigInt type
// Symbol type

// Non-Primitive Type:

// Objects


// String - Can use double quotes, single quotes or back tick 
const name = "Sam";
const language = 'English';
const lastName = `Smith`;

// Number
const total = 8;
const PI = 3.14;

// Boolean - Represents logical entities and can only be true or false. Normally used conditionally.
const isPrimaryNumber = true;
const isNewUser = false;


// Undefined - A value that is not yet been defined
let result
console.log(result); 
// You get undefined in the console.

// Null - Represents empty or unknown value
const data = null;

// Object - Is a complex data type. Object contains properties defined as key value pairs
const person = {
    "firstName": "Bruce", 
    "lastName": "Wayne",
    "age": 44
}
// To access a property in the object you can use dot notation

console.log(person.firstName);

// The whole syntax above for an object is known as object literal type.

// The other type is an array.

const oddNumbers = [1, 3, 5, 7, 9];
// Obtain value using postions in the array with first value being 0
console.log(oddNumbers[0]);

// Assignments - last defined value is what the value is.
let a = 34;
a = "Silly";
a = true;

console.log(a);





